import random

def gcd(a, b):
    while b != 0:
        a, b = b, a % b
    return a

def lcm(a, b):
    return (a * b) // gcd(a, b)

def generate_numbers():
    num1 = random.randint(1, 100)
    num2 = random.randint(1, 100)
    num3 = random.randint(1, 100)
    correct_answer = lcm(lcm(num1, num2), num3)
    return f'{num1} {num2} {num3}', correct_answer